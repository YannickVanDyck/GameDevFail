        public Level2(Hero hero, Game1 game, ContentManager content, GraphicsDevice graphicsDevice)
        {
            Hero = hero;
            _game = game;
            _content = content;
            _graphicsDevice = graphicsDevice;
        }

        public int test = 0;
        public void CreateWorld()
        {
            for (int x = 0; x < 32; x++)
            {
                for (int y = 0; y < 60; y++)
                {
                    if (tileArray[x, y] == 1)
                    {
                        Collides.Add(new GroundLayer(texture, new Vector2(y * 32, x * 32)));
                    }
                    if (tileArray[x, y] == 2)
                    {
                        Skeletons.Add(new Skeleton(skeletonLeftTexture, skeletonRightTexture, new Vector2(y * 32, x * 32)));
                    }
                }
            }
            CM = new CollisionManager(Hero, Collides, Skeletons, _game, _content, _graphicsDevice);
        }

        public void DrawWorld(SpriteBatch spriteBatch, GraphicsDevice device)
        {
            foreach (var item in Collides)
            {
                item.Draw(spriteBatch, device);
            }
        }

        public void DeleteWorld()
        {
            Collides.Clear();
        }

        //nog een updater maken
        public void Update(GameTime gameTime)
        {
            CM.CheckForCollision();
        }

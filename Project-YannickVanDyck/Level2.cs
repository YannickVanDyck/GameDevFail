        public Level2(Hero hero, Game1 game, ContentManager content, GraphicsDevice graphicsDevice)
        {
            HeroHard = hero;
            _game = game;
            _content = content;
            _graphicsDevice = graphicsDevice;
        }

        public int test = 0;
        public void CreateWorld()
        {
            for (int x = 0; x < 32; x++)
            {
                for (int y = 0; y < 60; y++)
                {
                    if (tileArray[x, y] == 1)
                    {
                        Collides.Add(new GroundLayer(texture, new Vector2(y * 32, x * 32)));
                    }
                    if (tileArray[x, y] == 2)
                    {
                        Skeletons.Add(new Skeleton(skeletonLeftTexture, skeletonRightTexture, new Vector2(y * 32, x * 32)));
                    }
                    if (tileArray[x, y] == 3)
                    {
                        Hero.Add(new Hero(heroLeftTexture, heroRightTexture, new Vector2(y * 32, x * 32), _game));
                    }
                    if (tileArray[x, y] == 4)
                    {
                        Coins.Add(new Coin(Coin, new Vector2(y * 32, x * 32)));
                    }
                }
            }
            CM = new CollisionManager(Collides,Coins, Skeletons, Hero, _game, _content, _graphicsDevice);
        }

        public void DrawWorld(SpriteBatch spriteBatch, GraphicsDevice device)
        {
            foreach (var item in Skeletons)
            {
                item.Draw(spriteBatch, device);
            }
            foreach (var item in Hero)
            {
                item.Draw(spriteBatch, device);
            }
            foreach (var item in Coins)
            {
                item.Draw(spriteBatch, device);
            }
            foreach (var item in Collides)
            {
                item.Draw(spriteBatch, device);
            }
        }

        public void DeleteWorld()
        {
            Collides.Clear();
        }

        //nog een updater maken
        public void Update(GameTime gameTime)
        {
            foreach (var item in Skeletons)
            {
                item.Update(gameTime);
            }
            foreach (Hero item in Hero)
            {
                item._controls = new ZQSDControl();
                item.Update(gameTime);
            }
            foreach (var item in Coins)
            {
                item.Update(gameTime);
            }
            CM.CheckForCollision();
        }
